#include<iostream>
using namespace std;

int main()
{
    int x,y,z;
    cout<<"\n Enter x : ";
    cin>>x;
    cout<<"\n Enter y : ";
    cin>>y;
    cout<<"\n Enter z : ";
    cin>>z;
    cout<<"\n (x+y).z = "<<(x+y)*z;
    cout<<"\n\n x.y+y.z = "<<x*y+y*z;
    cout<<"\n\n";
    return 0;
}
